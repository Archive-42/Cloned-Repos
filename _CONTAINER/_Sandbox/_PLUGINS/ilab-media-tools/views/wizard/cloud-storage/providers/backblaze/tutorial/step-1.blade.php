<h1 id="setting-up-backblaze">Setting Up Backblaze</h1>
<h2 class='track-pos' id='step-1-create-bucket'>Step 1 &#8211; Create Bucket</h2>
<p>To get started, log into your Backblaze account and click on the&nbsp;<strong>Buckets</strong>section. When the page had loaded, click on&nbsp;<strong>Create a Bucket</strong>.</p>
<figure class="wp-block-image"><img src="https://i.imgur.com/ylatxL7.png" alt="image.png"/></figure>
<h3 class='track-pos' id='step-1-1-bucket-properties'>Step 1.1 &#8211; Bucket Properties</h3>
<p>In the pop-up dialog, give your bucket a name and&nbsp;<strong>make sure Files in Bucket is set to Public</strong>. If&nbsp;<strong>Files in Bucket</strong>&nbsp;is set to Private, it will not work.</p>
<figure class="wp-block-image"><img src="https://i.imgur.com/bXfUlgg.png" alt="image.png"/></figure>
<p>Finally, click on&nbsp;<strong>Create a Bucket</strong></p>
